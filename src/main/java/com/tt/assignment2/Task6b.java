//Java program to illustrate public modifier
package com.tt.assignment2;
//Class "Task6b" is having Public access modifier
public class Task6b {
	public void sub() {
		int a = 700;
		int b = 300;
		int sub = a-b;
		System.out.println(sub);
	}// end of sub method
}// end of main method

/*In public modifier in java, there is no restriction,
 * Java data types or methods can access from anywhere in the program.
 * It has the widest scope amongst all the modifiers.*/